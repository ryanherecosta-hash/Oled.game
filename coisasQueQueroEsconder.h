class Timer {
public:
	unsigned long tempoAnterior;
	Timer(){
		tempoAnterior =0;
	}
	bool tempo(unsigned long temporizador){
		if (millis() - tempoAnterior >= temporizador){
			tempoAnterior = millis();
			return true;
		}
		return false;
	}
	void reset(){
		tempoAnterior = millis();
	}

};

